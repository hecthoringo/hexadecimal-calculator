	;ASCII_ENTERO
	;Convierte caracter numerico de ASCII a entero
	;@param	AL	Codigo ASCII a convertir
	;@ret	AX	Numero entero hexadecimal DE 16 bits
	
	ASCII_ENTERO PROC NEAR
		MOV AH,00H	
	NUMEROS:
		CMP AL,30H	;NUMERO
		JL LETRASM
		CMP AL,39H
		JG LETRASM
		SUB AL,30H
	LETRASM:		
		CMP AL,41H	;MAYUSCULA
		JL FIN
		CMP AL,46H
		JG FIN
		SUB AL,37H
    FIN:
		RET
	ASCII_ENTERO ENDP
	
	;ROTAR_NUMERO
	;Rota a 4 bits a la izquierda
	;@param	AL	Codigo ASCII a ROTAR_NUMERO
	;@ret	AX	Numero entero hexadecimal DE 16 bits
	
	ROTAR_NUMERO PROC NEAR
		MOV DL,44
		MOV DH,3
		CALL POSICIONAR_CURSOR
		
		CALL ASCII_ENTERO
		
		CMP BP,1
		JE SIGNUM
			MOV CL,4
			SHL NUM1,CL
			ADD NUM1,AX
			MOV AX,NUM1
		JMP FINEMP
	SIGNUM:
			MOV CL,4
			SHL NUM2,CL
			ADD NUM2,AX
			MOV AX,NUM2		
	FINEMP:
		CALL IMPRIMIR_REGISTRO
		RET
	ROTAR_NUMERO ENDP
	
	;IMPRIMIR_REGISTRO
	;Muestra los numeros entero del registro en hexadecimal
	;@param	AX	Registro de 16 bits
	;@ret
	
	IMPRIMIR_REGISTRO PROC NEAR
		MOV CX,0004H
		MOV DX,0000
		ENTERO_ASCII:
			MOV BX,10H ;DIVISOR
			DIV BX
		NUMERO:
			CMP DL,0
			JL LETRA
			CMP DL,9
			JG LETRA
			ADD DL,30H
		LETRA:
			CMP DL,10
			JL CONTINUA
			CMP DL,15
			JG CONTINUA
			ADD DL,37H
		CONTINUA:
			MOV DH,0H
			PUSH DX
			MOV DX,0H
		LOOP ENTERO_ASCII
		
		MOV CX,0004H
		ESCRIBE:
			POP DX
			CALL ESCRIBIR_CARACTER
		LOOP ESCRIBE
		RET
		
	IMPRIMIR_REGISTRO ENDP
	
	;OPERAR
	;Realiza las cuatro operaciones basicas
	;@param	DI	Identificador de operacion ('S':53H, 'R':52H, 'M':4DH, 'D':44H,)
	;@param	AX	Numero 1
	;@param	DX	Numero 2
	;@ret	AX	Registro resultante de alguna operacion
	
	OPERAR PROC NEAR		
	SUMAR:
		CMP DI,2BH
		JNE RESTAR
			ADD AX,BX
			MOV NUM1,AX
		JMP FINOPERACIONES
	RESTAR:
		CMP DI,2DH
		JNE MULTIPLICAR
			SUB AX,BX
			MOV NUM1,AX
		JMP FINOPERACIONES
	MULTIPLICAR:
		CMP DI,78H
		JNE DIVIDIR
			MUL BX
			MOV NUM1,AX
		JMP FINOPERACIONES
	DIVIDIR:	
		CMP DI,246
		JNE FINOPERACIONES
			CMP BX,0
			JNE HAZDIV
				MOV DL,37
				MOV DH,3
				CALL POSICIONAR_CURSOR
				LEA DX,MSJ_PORCERO
				CALL IMPRIMIR_CADENA				
		JMP FINOPERACIONES
		HAZDIV:
			DIV BL				
	FINOPERACIONES:
		CALL DISPLAY_CALCULADORA
		MOV AX,0000H
		MOV BX,0000H
		MOV NUM2,0000H
		RET
	OPERAR ENDP
	
	;DISPLAY_CALCULADORA
	;Muestra un formulario de contraseña
	;@param
	;@ret
	
	DISPLAY_CALCULADORA PROC NEAR
		
		PUSH AX
		MOV BH,1FH		
		MOV CL,26
