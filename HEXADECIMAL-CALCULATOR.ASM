	;LEER_TECLADO_CALCULADORA
	;Lee el teclado de la calculadora
	;@param
	;@ret
	
	LEER_TECLADO_CALCULADORA PROC NEAR
		CALL LEER_CURSOR
	SUM:
		CMP AL,2BH
		JNE RES
			MOV BP,1
			MOV DI,2BH
		JMP FT
	RES: 
		CMP AL,2DH
		JNE MULTI
			MOV BP,1
			MOV DI,2DH
		JMP FT
	MULTI:
		CMP AL,78H
		JNE DIVI
			MOV BP,1
			MOV DI,78H
		JMP FT
	DIVI:
		CMP AL,246
		JNE IGU
			MOV BP,1
			MOV DI,246
		JMP FT
	IGU:
		CMP AL,3DH
		JNE BORR
			MOV AX,NUM1
			MOV BX,NUM2
			CALL OPERAR
		JMP FT
	BORR:
		CMP AL, 4CH
		JNE SIGNUMS
			CALL RESTABLECER			
			MOV DL,X1
			MOV DH,Y1
			CALL POSICIONAR_CURSOR
		CALL FT
	SIGNUMS:
		CALL ROTAR_NUMERO
	FT: 
		RET
	LEER_TECLADO_CALCULADORA ENDP
	
	;MUEVE_ARRIBA
